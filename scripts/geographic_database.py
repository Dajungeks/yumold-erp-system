    def get_countries(self):
        """지원하는 국가 목록을 반환합니다."""
        return list(self.geographic_data.keys())
    
    def get_regions(self, country):
        """특정 국가의 주/도/성 목록을 반환합니다."""
        if country in self.geographic_data:
            return list(self.geographic_data[country].keys())
        return []
    
    def get_cities(self, country, region=None):
        """특정 지역의 도시 목록을 반환합니다."""
        if region:
            # 특정 주/도/성의 도시들
            if country in self.geographic_data and region in self.geographic_data[country]:
                return self.geographic_data[country][region]
        else:
            # 국가 전체 도시들 (모든 지역 통합)
            if country in self.geographic_data:
                all_cities = []
                for region_cities in self.geographic_data[country].values():
                    all_cities.extend(region_cities)
                return sorted(list(set(all_cities)))  # 중복 제거 및 정렬
        return []
    
    def search_city(self, country, city_name):
        """도시명으로 해당 지역을 찾습니다."""
        if country in self.geographic_data:
            for region, cities in self.geographic_data[country].items():
                if city_name in cities:
                    return region
        return None
    
    def get_all_cities_flat(self, country):
        """국가의 모든 도시를 평면 리스트로 반환합니다."""
        if country in self.geographic_data:
            all_cities = []
            for cities in self.geographic_data[country].values():
                all_cities.extend(cities)
            return sorted(list(set(all_cities)))
        return []
    
    def get_region_info(self, country, city_name):
        """도시명으로 지역 정보를 반환합니다."""
        region = self.search_city(country, city_name)
        if region:
            return {
                "country": country,
                "region": region,
                "city": city_name
            }
        return None
    
    def get_cities_by_country(self, country):
        """특정 국가의 모든 도시 목록을 반환합니다."""
        return self.get_all_cities_flat(country)

